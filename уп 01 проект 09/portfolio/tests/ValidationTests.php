<?php

function runValidationTests($runner) {
    // Тест 1: Валидный номер телефона
    $runner->runTest("Валидация: Корректный номер +7", function() {
        assert(validatePhone("+79221110500") === true, "Номер +79221110500 должен быть валиден");
    });

    // Тест 2: Номер без +7
    $runner->runTest("Валидация: Номер без +7 невалиден", function() {
        assert(validatePhone("79221110500") === false, "Номер без +7 должен быть невалиден");
    });

    // Тест 3: Номер с кодом 8 вместо +7
    $runner->runTest("Валидация: Номер с кодом 8 невалиден", function() {
        assert(validatePhone("+89221110500") === false, "Номер с кодом 8 должен быть невалиден");
    });

    // Тест 4: Номер слишком короткий
    $runner->runTest("Валидация: Номер слишком короткий", function() {
        assert(validatePhone("+792211105") === false, "Номер с 9 цифрами должен быть невалиден");
    });

    // Тест 5: Номер слишком длинный
    $runner->runTest("Валидация: Номер слишком длинный", function() {
        assert(validatePhone("+7922111050012") === false, "Номер с 11+ цифрами должен быть невалиден");
    });

    // Тест 6: Пароль минимальной длины (6 символов)
    $runner->runTest("Валидация: Пароль 6 символов валиден", function() {
        assert(validatePassword("Test12") === true, "Пароль из 6 символов должен быть валиден");
    });

    // Тест 7: Пароль максимальной длины (10 символов)
    $runner->runTest("Валидация: Пароль 10 символов валиден", function() {
        assert(validatePassword("Test123456") === true, "Пароль из 10 символов должен быть валиден");
    });

    // Тест 8: Пароль слишком короткий (5 символов)
    $runner->runTest("Валидация: Пароль 5 символов невалиден", function() {
        assert(validatePassword("Test1") === false, "Пароль из 5 символов должен быть невалиден");
    });

    // Тест 9: Пароль слишком длинный (11 символов)
    $runner->runTest("Валидация: Пароль 11 символов невалиден", function() {
        assert(validatePassword("Test12345678") === false, "Пароль из 11 символов должен быть невалиден");
    });

    // Тест 10: Пустой пароль
    $runner->runTest("Валидация: Пустой пароль невалиден", function() {
        assert(validatePassword("") === false, "Пустой пароль должен быть невалиден");
    });
}
?>
